    private void ActionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActionButtonActionPerformed
        String login = LoginTextField.getText();
        String password = PasswordTextField.getText();
        String response;
        String [] part;
        try {
            client = new ClientMUCOP("127.0.0.1", 6002);
        } catch (IOException ex) {
            Logger.getLogger(ClientMUCOP.class.getName()).log(Level.SEVERE, null, ex);
            System.exit(1);
        }
        response = client.login(login, password);
        part = response.split("#");
        if(part[0].equals("ERR"))
        {
            ResponseLabel.setText(part[1]);
            ResponseLabel.setVisible(true);
            return;
        }
        else
        {
            parent.setAdresse(part[1]);
            parent.setPort(part[2]);
            parent.setUser(login);
            parent.isLogged();
        }
    }//GEN-LAST:event_ActionButtonActionPerformed

