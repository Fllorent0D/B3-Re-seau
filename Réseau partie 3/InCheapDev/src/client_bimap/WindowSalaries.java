    public WindowSalaries(java.awt.Frame parent, boolean modal, Message response) {
        super(parent, modal);
        initComponents();
        
        setLocationRelativeTo(null);
        
        SalariesTable.setModel(new DefaultTableModel(new String[]{"Id", "Montant", "Personnel"}, 0));
        
        if(response.hasParam("error"))
            JOptionPane.showMessageDialog(this, response.getParam("error"));
        else
        {
            DefaultTableModel dtm = (DefaultTableModel) SalariesTable.getModel();
            
            dtm.setRowCount(0);
            
            ArrayList<Salaire> data = (ArrayList) response.getParam("data");

            data.forEach((row) -> {
                dtm.addRow(new Object[]{row.getIdSalaire(), row.getMontant(), row.getPersonnel().getPrenom() + " " + row.getPersonnel().getNom()});
            });
        } 
    }

